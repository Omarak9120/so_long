#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int				i ;
	unsigned char	ch ;

	if (!str)
		return (NULL);
	ch = c;
	i = 0;
	if (ch == '\0')
	{
		i = ft_strlen(str);
		return ((char *)str + i++);
	}
	while (str[i])
	{
		if (str[i] == ch)
			return ((char *)str + i);
		i++;
	}
	return (NULL);
}
